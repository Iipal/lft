#include "libft.h"

int32_t	ft_strncmp(const char *s1, const char *s2, size_t n)
{
	uint8_t	u1;
	uint8_t	u2;

	u1 = 1;
	while (n-- && u1)
	{
		u1 = (uint8_t)*s1++;
		u2 = (uint8_t)*s2++;
		if (u1 != u2)
			return (u1 - u2);
	}
	return (0);
}
